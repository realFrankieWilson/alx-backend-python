#!/usr/bin/env python3
"""
Module `3-to_str`
This module provides a type-annotated function that returns
the string representation of a float

Function:
  to_str(n: float) -> str:
      Converts a float to a string and return the result.

      Arg:
          n (float): an argument of type float.

      Returns: The string representation of the float `n`.
"""


def to_str(n: float) -> str:
    """
    Converts a float to a string and returns a string value.

    Args:
        n (int): the float value.

    returns:
        A string value.
    """
    return str(n)
